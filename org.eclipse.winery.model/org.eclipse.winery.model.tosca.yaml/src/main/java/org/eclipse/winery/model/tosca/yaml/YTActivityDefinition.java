import org.eclipse.winery.model.tosca.yaml.visitor.IVisitor;
import org.eclipse.winery.model.tosca.yaml.visitor.VisitorNode;

/**
 * This is an abstract parent class for the following TOSCA activity definitions: Delegate workflow activity definition,
 * Set state activity definition, Call operation activity definition, Inline workflow activity definition
 */
public abstract class YTActivityDefinition implements VisitorNode {
    public abstract <R extends AbstractResult<R>, P extends AbstractParameter<P>> R accept(IVisitor<R, P> visitor, P parameter);
}
